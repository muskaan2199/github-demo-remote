#Add implementation
def add(x,y):
  return(x+y)

#Subtract implementation  
def subtract(x,y):
  return(x-y)

#Multiplication implementation  
def multiply(x,y):
  return(x*y)

#Division implementation  
def divide(x,y):
  if y==0:
    return DIVIDEBYZEROERROR
  else:
    return(x/y) 

def square(x):
  return(x*x)

